CREATE TABLE USERS 
(
	ID VARCHAR(37) NOT NULL,
	FIRST_NAME VARCHAR(400) NOT NULL,
	LAST_NAME VARCHAR(200),
	ROLE VARCHAR(37) NOT NULL,
	EMAIL VARCHAR(400) UNIQUE NOT NULL,
	ALTERNATIVE_EMAIL VARCHAR (400) UNIQUE NOT NULL,
	GENDER_ID VARCHAR(37) NOT NULL,
	DOB TIMESTAMP,
	PASSWORD TEXT NOT NULL,
	PHONE_NO   VARCHAR(15),
	COUNTRY_ID   VARCHAR(37) ,
	CREATED_SID   VARCHAR(37) NOT NULL,
	CREATED_BY    VARCHAR(37) NOT NULL,
	CREATED_ON    TIMESTAMP NOT NULL,
	UPDATED_SID   VARCHAR(37),
	UPDATED_BY    VARCHAR(37),
	UPDATED_ON    TIMESTAMP,
	PRIMARY KEY (ID),
	FOREIGN KEY (GENDER_ID) REFERENCES GENDER(ID),
	FOREIGN KEY (ROLE) REFERENCES ROLES (ID),
	FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY (ID)
) ;

CREATE TABLE ADD_PLAN
(
    ID VARCHAR(37) NOT NULL,
	PLAN_NAME VARCHAR(400),
	PLAN_PRICE FLOAT,
	GATEWAY_TYPE_ID  VARCHAR(37) NOT NULL    ,
	NO_OF_TASKS INTEGER,
	COST_PER_SHARE FLOAT,
	CURRENCY_ID VARCHAR(37) NOT NULL,
	COUNTRY_ID   VARCHAR(37) ,
	EXPIRY_DATE  TIMESTAMP,
	STATUS VARCHAR(37) NOT NULL,
	PRIMARY KEY (ID),
	FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY (ID),
	FOREIGN KEY (STATUS) REFERENCES STATUS (ID),
	FOREIGN KEY (GATEWAY_TYPE_ID) REFERENCES GATE_TYPE (ID)
) ;


CREATE TABLE WORKSHEET
(
    ID INTERGER NOT NULL AUTO_INCREMENT,
	GATEWAY_TYPE_ID  VARCHAR(37) NOT NULL    ,
	EMAIL VARCHAR(400) UNIQUE ,
	PHONE_NO   VARCHAR(15),
    PRODUCT_NAME VARCHAR(300) NOT NULL,
	PRODUCT_URL   VARCHAR(1000) NOT NULL, 
    DESCRIPTION   VARCHAR(1000) NOT NULL, 
	FOREIGN KEY (STATUS) REFERENCES STATUS (ID),
	FOREIGN KEY (GATEWAY_TYPE_ID) REFERENCES GATE_TYPE (ID)

)